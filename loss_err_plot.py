import numpy as np
import matplotlib.pyplot as plt
data1 = np.load('./results/Poisson2D1Peak/Poisson2D1Peak_Uni_resample_19076082_1000e-pp1/train/loss_err.npy')
data2 = np.load('./results/Poisson2D1Peak/Poisson2D1Peak_RAD_resample_19076082_1000e-pp1/train/loss_err.npy')
data3 = np.load('./results/Poisson2D1Peak/Poisson2D1Peak_AAIS_g_resample_19076082_1000e-pp1/train/loss_err.npy')
data4 = np.load('./results/Poisson2D1Peak/Poisson2D1Peak_AAIS_t_resample_19076082_1000e-pp1/train/loss_err.npy')
fig, ax = plt.subplots(layout='constrained', figsize=(19.2, 4.8))
ax.semilogy(np.array(data1[:, 0]), label='Uni')
ax.semilogy(np.array(data2[:, 0]), label='RAD')
ax.semilogy(np.array(data3[:, 0]), label='AAIS-g')
ax.semilogy(np.array(data4[:, 0]), label='AAIS-t')
ax.axhline(y=1e-1,color='black', linestyle='--', linewidth=1.0)
ax.set_ylim(1e-3, 1e4)
ax.legend(loc='lower left', fontsize=12)
fig.savefig('./results/Poisson2D1Peak/resample_1000e_loss.png',dpi=300)
plt.show()
fig, ax = plt.subplots(layout='constrained', figsize=(19.2, 4.8))
ax.semilogy(np.array(data1[:, 1]), label='Uni')
ax.semilogy(np.array(data2[:, 1]), label='RAD')
ax.semilogy(np.array(data3[:, 1]), label='AAIS-g')
ax.semilogy(np.array(data4[:, 1]), label='AAIS-t')
ax.axhline(y=1e-1, color='black', linestyle='--', linewidth=1)
ax.set_ylim(8e-3, 10)
ax.legend(loc='lower left', fontsize=12)
fig.savefig('./results/Poisson2D1Peak/resample_1000e_err.png',dpi=300)
plt.show()
